function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Jake's Pizza Co.</h1>
    </header>
  );
}

function Menu() {
  const pizzas = pizzaData;
  const numPizzas = pizzas.length;

  return (
    <main className="menu">
      <h2>Our Menu</h2>

      <p>
        Authentic Italian cuisine. 6 creative dissions to choose from. Our coal
        fired, brick oven pizzas are to-die-for! That's what she said!
      </p>

      {numPizzas > 0 ? (
        <ul className="pizzas">
          {pizzaData.map((pizza) => (
            <Pizza pizzaObj={pizza} key={pizza.name} />
          ))}
        </ul>
      ) : (
        <p>Sorry, we're still working on our menu.</p>
      )}
    </main>
  );
}

function Pizza({ pizzaObj }) {
  return (
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObj.photoName} alt={pizzaObj.name}></img>
      <div>
        <h2>{pizzaObj.name}</h2>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SOLD OUT" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const time = new Date().toLocaleTimeString();
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 20;
  let isOpen = hour >= openHour && hour <= closeHour;
  let openText = isOpen
    ? `We are sooo open and ready for business!`
    : `Oh snap, you really missed it dummy! Our hours are ${openHour}:00 to ${closeHour}:00 and it's ${time}. We are sooo closed now! Gahh!`;

  return (
    <footer className="footer">
      <Order openText={openText} isOpen={isOpen} />
    </footer>
  );
}

function Order({ openText, isOpen }) {
  return (
    <div className="order">
      <p>{openText}</p>
      {isOpen && <button className="btn">Order</button>}
    </div>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
